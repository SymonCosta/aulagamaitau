package exercicios;

public class Exercicioforwhile {
    public static void main(String[] args) {
        int a, i;
   
        a = 10;
        i = 0;
   
        while (i < 3) {
           System.out.println(a = a + i);
           i++;
        }
       
        System.out.println("a = " + a);
   
}

}
